package org.example.template.primitive.functional;

public interface Supplier {

    interface Byte {
        byte get();
    }

    interface Short {
        short get();
    }

    interface Int {
        int get();
    }

    interface Long {
        long get();
    }

    interface Char {
        char get();
    }

    interface Bool {
        boolean get();
    }

    interface Float {
        float get();
    }

    interface Double {
        double get();
    }

}